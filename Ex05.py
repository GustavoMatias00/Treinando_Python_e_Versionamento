#Desafio de mostrar o antecessor e o sucessor(sem variável)
numero = int(input("Digite um número: "))
print(f"Seu antecessor é {numero - 1} e seu sucessor é { numero + 1}")

#Desafio de mostrar o antecessor e o sucessor(com variável)
numero = int(input("Digite um número: "))
antecessor = numero - 1
sucessor = numero + 1
print(f"Seu antecessor é {antecessor} e seu sucessor é {sucessor}")