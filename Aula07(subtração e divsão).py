#Desafio de subtrair e dividir dois números
primeiro_numero = input("Digite um número: ")
segundo_numero = input("Digite outro número: ")
print(f"A subtração de {primeiro_numero} por {segundo_numero} é {int(primeiro_numero) - int(segundo_numero)}")
print(f"A divisão de {primeiro_numero} por {segundo_numero} é {int(primeiro_numero) / int(segundo_numero)}")